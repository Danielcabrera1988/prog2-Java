package POO;

public class Corcho {
    String bodega;
    boolean enBotella = true;

    public String getBodega() {
        return bodega;
    }

    public void setBodega(String bodega) {
        this.bodega = bodega;
    }

    public boolean isEnBotella() {
        return enBotella;
    }

    public void setEnBotella(boolean enBotella) {
        this.enBotella = enBotella;
    }
}
